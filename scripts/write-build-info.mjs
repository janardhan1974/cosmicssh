// Generates src/main/build-info.ts with the current build timestamp.
// Wired as `prebuild` in package.json so it runs automatically before every
// `npm run build` (and therefore every `npm run dist*` too).
//
// The file it writes is committed as a stub; this script overwrites it. The
// resulting diff just shows the timestamp moving forward — accepted noise.

import { writeFileSync } from 'node:fs'
import { fileURLToPath } from 'node:url'
import { dirname, resolve } from 'node:path'

const here = dirname(fileURLToPath(import.meta.url))
const out = resolve(here, '..', 'src', 'main', 'build-info.ts')

const now = new Date()
const iso = now.toISOString()
// Locale-formatted display string, e.g. "May 14, 2026, 12:34 PM"
const display = now.toLocaleString('en-US', {
  year: 'numeric',
  month: 'short',
  day: 'numeric',
  hour: 'numeric',
  minute: '2-digit',
  hour12: true,
})
// Date-stamped version, e.g. "05.14.2026". Used in Help → About so the
// version visibly bumps every build (every code change → new build → new
// version). package.json's static "version" is still used by electron-builder
// for installer artifact names, so we don't touch that here.
const mm = String(now.getMonth() + 1).padStart(2, '0')
const dd = String(now.getDate()).padStart(2, '0')
const yyyy = String(now.getFullYear())
const buildVersion = `${mm}.${dd}.${yyyy}`

const content = `// AUTO-GENERATED by scripts/write-build-info.mjs.
// Overwritten on every build. Do not edit by hand.

export const BUILD_DATE_ISO = '${iso}'
export const BUILD_DATE_DISPLAY = '${display}'
export const BUILD_VERSION = '${buildVersion}'
`

writeFileSync(out, content, 'utf8')
console.log(`[build-info] wrote v${buildVersion} (${display}) → ${out}`)
