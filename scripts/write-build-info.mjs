// Generates src/main/build-info.ts with the current build timestamp.
// Wired three ways:
//   - `prebuild` in package.json — every `npm run build` (and `dist*`).
//   - `predev`  in package.json — every `npm run dev` cold start.
//   - scripts/dev.mjs imports `stampBuildInfo` and calls it on every
//     main/preload/shared source change so Help → About reflects the latest
//     code change after each Electron restart (not just the dev-start time).
//
// The file it writes is committed as a stub; this script overwrites it. The
// resulting diff just shows the timestamp moving forward — accepted noise.

import { writeFileSync } from 'node:fs'
import { fileURLToPath } from 'node:url'
import { dirname, resolve } from 'node:path'

const here = dirname(fileURLToPath(import.meta.url))
const out = resolve(here, '..', 'src', 'main', 'build-info.ts')

export function stampBuildInfo() {
  const now = new Date()
  const iso = now.toISOString()
  // Locale-formatted display string, e.g. "May 15, 2026, 12:50:30 PM".
  // Seconds matter — rapid same-minute saves would otherwise produce the same
  // string and look like nothing changed.
  const display = now.toLocaleString('en-US', {
    year: 'numeric',
    month: 'short',
    day: 'numeric',
    hour: 'numeric',
    minute: '2-digit',
    second: '2-digit',
    hour12: true,
  })
  // Date+time-stamped version, e.g. "05.15.2026.12.50.30". Shown in Help →
  // About so the version visibly bumps on every code change. Second-precision
  // for the same reason as the display string above. package.json's static
  // "version" is still used by electron-builder for installer artifact names,
  // so we don't touch that here.
  const mm = String(now.getMonth() + 1).padStart(2, '0')
  const dd = String(now.getDate()).padStart(2, '0')
  const yyyy = String(now.getFullYear())
  const hh = String(now.getHours()).padStart(2, '0')
  const min = String(now.getMinutes()).padStart(2, '0')
  const ss = String(now.getSeconds()).padStart(2, '0')
  const buildVersion = `${mm}.${dd}.${yyyy}.${hh}.${min}.${ss}`

  const content = `// AUTO-GENERATED by scripts/write-build-info.mjs.
// Overwritten on every build, and on every main-side src change in dev mode.
// Do not edit by hand.

export const BUILD_DATE_ISO = '${iso}'
export const BUILD_DATE_DISPLAY = '${display}'
export const BUILD_VERSION = '${buildVersion}'
`

  writeFileSync(out, content, 'utf8')
  return { buildVersion, display, out }
}

// Run as a script when invoked directly (`node scripts/write-build-info.mjs`).
// fileURLToPath handles Windows backslash vs forward-slash quirks correctly,
// so this comparison works on every platform.
if (process.argv[1] === fileURLToPath(import.meta.url)) {
  const { buildVersion, display, out: outPath } = stampBuildInfo()
  // eslint-disable-next-line no-console
  console.log(`[build-info] wrote v${buildVersion} (${display}) → ${outPath}`)
}
